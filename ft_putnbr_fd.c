void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		return (ft_putstr_fd(fd, "-2147483648"));
	if (n < 0)
	{
		ft_putchar_fd(fd, '-');
		n = n * (-1);
	}
	if (n >= 10)
	{
		ft_putnbr_fd(fd, n / 10);
		ft_putchar_fd(fd, (n % 10) + 48);
	}
	else
		ft_putchar_fd(fd, (n) + 48);
}
